def merge_job_titles(*args):
    all_job_titles = itertools.chain(series.unique() for series in (read_file(filename) for filename in args))
    unique_job_titles = set()
    for jobtitles in (set(jobtitles) for jobtitles in (jobtitles for jobtitles in all_job_titles)):
        for jobtitle in jobtitles:
            unique_job_titles.add(jobtitle)
    write_file('job_titles', sorted(unique_job_titles))


def read_file(filename):
    df = pandas.read_csv(filename + '.tsv', delimiter=';', names=['job_name'])
    return df['job_name']


def write_file(filename, items):
    with open(filename + '.tsv', 'w+', encoding='utf-8') as outfile:
        for item in items:
            outfile.write(item + '\n')


